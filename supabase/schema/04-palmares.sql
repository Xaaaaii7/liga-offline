-- Palmarés de competición (introspectado 2026-07-07).
CREATE TABLE competition_palmares (
  id                              integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  competition_id                  integer NOT NULL REFERENCES competitions(id) ON DELETE CASCADE,
  season                          varchar NOT NULL,
  winner_team_id                  integer REFERENCES league_teams(id) ON DELETE SET NULL,
  runner_up_team_id               integer REFERENCES league_teams(id) ON DELETE SET NULL,
  top_scorer_team_id              integer REFERENCES league_teams(id) ON DELETE SET NULL,
  best_defense_team_id            integer REFERENCES league_teams(id) ON DELETE SET NULL,
  most_possession_team_id         integer REFERENCES league_teams(id) ON DELETE SET NULL,
  cleanest_team_id                integer REFERENCES league_teams(id) ON DELETE SET NULL,
  most_accurate_passes_team_id    integer REFERENCES league_teams(id) ON DELETE SET NULL,
  most_efficient_shooting_team_id integer REFERENCES league_teams(id) ON DELETE SET NULL,
  most_effective_defense_team_id  integer REFERENCES league_teams(id) ON DELETE SET NULL,
  mvp_team_id                     integer REFERENCES league_teams(id) ON DELETE SET NULL,
  top_scorer_player_id            integer REFERENCES players(id) ON DELETE SET NULL,
  top_scorer_goals                integer,
  mvp_player_id                   integer REFERENCES players(id) ON DELETE SET NULL,
  best_defender_player_id         integer REFERENCES players(id) ON DELETE SET NULL,
  best_midfielder_player_id       integer REFERENCES players(id) ON DELETE SET NULL,
  best_forward_player_id          integer REFERENCES players(id) ON DELETE SET NULL,
  best_goalkeeper_player_id       integer REFERENCES players(id) ON DELETE SET NULL,
  auto_generated                  boolean DEFAULT true,
  created_at                      timestamptz DEFAULT now(),
  updated_at                      timestamptz DEFAULT now(),
  golden_boy_player_id            integer REFERENCES players(id) ON DELETE SET NULL,
  UNIQUE (competition_id, season)
);
