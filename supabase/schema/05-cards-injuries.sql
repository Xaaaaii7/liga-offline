-- Tarjetas amarillas, lesiones y sanciones (introspectado 2026-07-07).
CREATE TABLE match_yellow_cards (
  id                 bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  match_id           text NOT NULL,
  match_uuid         integer NOT NULL REFERENCES matches(match_uuid),
  league_team_id     integer NOT NULL REFERENCES league_teams(id),
  player_id          bigint REFERENCES players(id),
  minute             integer,
  competition_id     integer REFERENCES competitions(id),
  season             text,
  created_at         timestamptz DEFAULT now(),
  vod_offset_seconds integer
);

CREATE TABLE match_injuries (
  id             bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  match_id       text NOT NULL,
  league_team_id bigint NOT NULL REFERENCES league_teams(id) ON DELETE CASCADE,
  player_id      bigint NOT NULL REFERENCES players(id) ON DELETE CASCADE,
  created_at     timestamptz DEFAULT now(),
  competition_id integer REFERENCES competitions(id),
  season         text,
  match_uuid     integer NOT NULL REFERENCES matches(match_uuid) ON DELETE CASCADE,
  UNIQUE (match_id, player_id)
);

CREATE TABLE player_suspensions (
  id                bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  player_id         bigint NOT NULL REFERENCES players(id),
  league_team_id    bigint NOT NULL REFERENCES league_teams(id),
  match_id          text NOT NULL,
  origin_match_id   text NOT NULL,
  reason            text DEFAULT 'red_card',
  created_at        timestamptz DEFAULT now(),
  competition_id    integer REFERENCES competitions(id),
  season            text,
  match_uuid        integer REFERENCES matches(match_uuid) ON DELETE SET NULL,
  origin_match_uuid integer REFERENCES matches(match_uuid) ON DELETE SET NULL,
  UNIQUE (player_id, match_id)
);
