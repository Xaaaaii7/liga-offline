CREATE OR REPLACE FUNCTION classify_position(pos text)
 RETURNS character varying
 LANGUAGE plpgsql
 IMMUTABLE
AS $function$
DECLARE
    pos_lower TEXT;
BEGIN
    IF pos IS NULL THEN RETURN NULL; END IF;
    pos_lower := LOWER(pos);
    IF pos_lower LIKE '%goalkeeper%' OR pos_lower LIKE '%portero%' THEN RETURN 'GK'; END IF;
    IF pos_lower LIKE '%defence%' OR pos_lower LIKE '%back%' OR pos_lower LIKE '%centre-back%' OR pos_lower LIKE '%defensa%' THEN RETURN 'DEF'; END IF;
    IF pos_lower LIKE '%midfield%' OR pos_lower LIKE '%medio%' OR pos_lower LIKE '%mid%' THEN RETURN 'MID'; END IF;
    IF pos_lower LIKE '%offence%' OR pos_lower LIKE '%forward%' OR pos_lower LIKE '%wing%' OR pos_lower LIKE '%striker%' OR pos_lower LIKE '%delantero%' THEN RETURN 'FWD'; END IF;
    RETURN NULL;
END;
$function$;

