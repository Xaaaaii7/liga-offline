CREATE OR REPLACE FUNCTION calculate_best_player_jornada(p_competition_id integer, p_season text, p_jornada integer)
 RETURNS void LANGUAGE plpgsql AS $function$
DECLARE
    v_best RECORD;
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM matches
        WHERE competition_id = p_competition_id AND round_id = p_jornada
          AND home_goals IS NOT NULL AND away_goals IS NOT NULL
          AND (resolved_administratively IS NULL OR resolved_administratively = false)
    ) THEN
        DELETE FROM best_player_jornada WHERE competition_id = p_competition_id AND season = p_season AND jornada = p_jornada;
        RETURN;
    END IF;

    SELECT INTO v_best
        prj.player_id, prj.avg_rating,
        (SELECT mpr.league_team_id FROM match_player_ratings mpr
         INNER JOIN matches m ON m.match_uuid = mpr.match_uuid
         WHERE mpr.player_id = prj.player_id AND m.competition_id = p_competition_id
           AND m.season = p_season AND m.round_id = p_jornada LIMIT 1) AS league_team_id,
        COALESCE(goal_counts.goals, 0) AS goals
    FROM player_ratings_jornada prj
    LEFT JOIN LATERAL (
        SELECT COUNT(*)::INTEGER AS goals FROM goal_events ge
        INNER JOIN matches m ON ge.match_uuid = m.match_uuid
        WHERE ge.player_id = prj.player_id AND ge.event_type = 'goal'
          AND m.competition_id = p_competition_id AND m.season = p_season AND m.round_id = p_jornada
    ) goal_counts ON true
    WHERE prj.competition_id = p_competition_id AND prj.season = p_season AND prj.jornada = p_jornada
    ORDER BY prj.avg_rating DESC NULLS LAST, goal_counts.goals DESC, prj.player_id ASC
    LIMIT 1;

    IF v_best.player_id IS NULL THEN
        DELETE FROM best_player_jornada WHERE competition_id = p_competition_id AND season = p_season AND jornada = p_jornada;
        RETURN;
    END IF;

    INSERT INTO best_player_jornada (competition_id, season, jornada, player_id, league_team_id, avg_rating, goals, updated_at)
    VALUES (p_competition_id, p_season, p_jornada, v_best.player_id, v_best.league_team_id, v_best.avg_rating, v_best.goals, NOW())
    ON CONFLICT (competition_id, season, jornada)
    DO UPDATE SET player_id = EXCLUDED.player_id, league_team_id = EXCLUDED.league_team_id,
                  avg_rating = EXCLUDED.avg_rating, goals = EXCLUDED.goals, updated_at = NOW();
END;
$function$;

CREATE OR REPLACE FUNCTION calculate_best_xi_jornada(p_competition_id integer, p_season text, p_jornada integer)
 RETURNS void LANGUAGE plpgsql AS $function$
DECLARE
    v_gk RECORD; v_def RECORD; v_mid RECORD; v_fwd RECORD; v_best_player RECORD;
    v_pos_classified VARCHAR(10);
    def_order INTEGER := 0; mid_order INTEGER := 0; fwd_order INTEGER := 0;
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM matches
        WHERE competition_id = p_competition_id AND round_id = p_jornada
          AND home_goals IS NOT NULL AND away_goals IS NOT NULL
          AND (resolved_administratively IS NULL OR resolved_administratively = false)
    ) THEN RETURN; END IF;

    DELETE FROM best_xi_jornada WHERE competition_id = p_competition_id AND season = p_season AND jornada = p_jornada;

    SELECT INTO v_best_player prj.*, COALESCE(goal_counts.goals, 0) as goals
    FROM player_ratings_jornada prj
    LEFT JOIN LATERAL (
        SELECT COUNT(*)::INTEGER as goals FROM goal_events ge
        INNER JOIN matches m ON ge.match_uuid = m.match_uuid
        WHERE ge.player_id = prj.player_id AND ge.event_type != 'own_goal'
          AND m.competition_id = p_competition_id AND m.season = p_season AND m.round_id = p_jornada
    ) goal_counts ON true
    WHERE prj.competition_id = p_competition_id AND prj.season = p_season AND prj.jornada = p_jornada
      AND classify_position(prj.position) IS NOT NULL
    ORDER BY prj.avg_rating DESC, goal_counts.goals DESC, prj.player_id ASC
    LIMIT 1;

    IF v_best_player IS NOT NULL THEN v_pos_classified := classify_position(v_best_player.position); END IF;

    SELECT INTO v_gk prj.*, COALESCE(goal_counts.goals, 0) as goals
    FROM player_ratings_jornada prj
    LEFT JOIN LATERAL (
        SELECT COUNT(*)::INTEGER as goals FROM goal_events ge
        INNER JOIN matches m ON ge.match_uuid = m.match_uuid
        WHERE ge.player_id = prj.player_id AND ge.event_type != 'own_goal'
          AND m.competition_id = p_competition_id AND m.season = p_season AND m.round_id = p_jornada
    ) goal_counts ON true
    WHERE prj.competition_id = p_competition_id AND prj.season = p_season AND prj.jornada = p_jornada
      AND classify_position(prj.position) = 'GK'
    ORDER BY prj.avg_rating DESC, goal_counts.goals DESC, prj.player_id ASC LIMIT 1;

    IF v_gk IS NOT NULL THEN
        INSERT INTO best_xi_jornada (competition_id, season, jornada, player_id, position, position_order, avg_rating, matches_count)
        VALUES (p_competition_id, p_season, p_jornada, v_gk.player_id, 'GK', 1, v_gk.avg_rating, v_gk.matches_count);
    END IF;

    def_order := 0;
    FOR v_def IN
        SELECT prj.*, COALESCE(goal_counts.goals, 0) as goals
        FROM player_ratings_jornada prj
        LEFT JOIN LATERAL (
            SELECT COUNT(*)::INTEGER as goals FROM goal_events ge
            INNER JOIN matches m ON ge.match_uuid = m.match_uuid
            WHERE ge.player_id = prj.player_id AND ge.event_type != 'own_goal'
              AND m.competition_id = p_competition_id AND m.season = p_season AND m.round_id = p_jornada
        ) goal_counts ON true
        WHERE prj.competition_id = p_competition_id AND prj.season = p_season AND prj.jornada = p_jornada
          AND classify_position(prj.position) = 'DEF'
        ORDER BY prj.avg_rating DESC, goal_counts.goals DESC, prj.player_id ASC LIMIT 4
    LOOP
        def_order := def_order + 1;
        INSERT INTO best_xi_jornada (competition_id, season, jornada, player_id, position, position_order, avg_rating, matches_count)
        VALUES (p_competition_id, p_season, p_jornada, v_def.player_id, 'DEF', def_order, v_def.avg_rating, v_def.matches_count);
    END LOOP;

    mid_order := 0;
    FOR v_mid IN
        SELECT prj.*, COALESCE(goal_counts.goals, 0) as goals
        FROM player_ratings_jornada prj
        LEFT JOIN LATERAL (
            SELECT COUNT(*)::INTEGER as goals FROM goal_events ge
            INNER JOIN matches m ON ge.match_uuid = m.match_uuid
            WHERE ge.player_id = prj.player_id AND ge.event_type != 'own_goal'
              AND m.competition_id = p_competition_id AND m.season = p_season AND m.round_id = p_jornada
        ) goal_counts ON true
        WHERE prj.competition_id = p_competition_id AND prj.season = p_season AND prj.jornada = p_jornada
          AND classify_position(prj.position) = 'MID'
        ORDER BY prj.avg_rating DESC, goal_counts.goals DESC, prj.player_id ASC LIMIT 3
    LOOP
        mid_order := mid_order + 1;
        INSERT INTO best_xi_jornada (competition_id, season, jornada, player_id, position, position_order, avg_rating, matches_count)
        VALUES (p_competition_id, p_season, p_jornada, v_mid.player_id, 'MID', mid_order, v_mid.avg_rating, v_mid.matches_count);
    END LOOP;

    fwd_order := 0;
    FOR v_fwd IN
        SELECT prj.*, COALESCE(goal_counts.goals, 0) as goals
        FROM player_ratings_jornada prj
        LEFT JOIN LATERAL (
            SELECT COUNT(*)::INTEGER as goals FROM goal_events ge
            INNER JOIN matches m ON ge.match_uuid = m.match_uuid
            WHERE ge.player_id = prj.player_id AND ge.event_type != 'own_goal'
              AND m.competition_id = p_competition_id AND m.season = p_season AND m.round_id = p_jornada
        ) goal_counts ON true
        WHERE prj.competition_id = p_competition_id AND prj.season = p_season AND prj.jornada = p_jornada
          AND classify_position(prj.position) = 'FWD'
        ORDER BY prj.avg_rating DESC, goal_counts.goals DESC, prj.player_id ASC LIMIT 3
    LOOP
        fwd_order := fwd_order + 1;
        INSERT INTO best_xi_jornada (competition_id, season, jornada, player_id, position, position_order, avg_rating, matches_count)
        VALUES (p_competition_id, p_season, p_jornada, v_fwd.player_id, 'FWD', fwd_order, v_fwd.avg_rating, v_fwd.matches_count);
    END LOOP;

    IF v_best_player IS NOT NULL AND v_pos_classified IS NOT NULL THEN
        IF v_best_player.player_id NOT IN (
            SELECT player_id FROM best_xi_jornada
            WHERE competition_id = p_competition_id AND season = p_season AND jornada = p_jornada
        ) THEN
            DELETE FROM best_xi_jornada
            WHERE competition_id = p_competition_id AND season = p_season AND jornada = p_jornada
              AND position = v_pos_classified
              AND player_id = (
                  SELECT bxi.player_id FROM best_xi_jornada bxi
                  LEFT JOIN LATERAL (
                      SELECT COUNT(*)::INTEGER as goals FROM goal_events ge
                      INNER JOIN matches m ON ge.match_uuid = m.match_uuid
                      WHERE ge.player_id = bxi.player_id AND ge.event_type != 'own_goal'
                        AND m.competition_id = p_competition_id AND m.season = p_season AND m.round_id = p_jornada
                  ) goal_counts ON true
                  WHERE bxi.competition_id = p_competition_id AND bxi.season = p_season AND bxi.jornada = p_jornada
                    AND bxi.position = v_pos_classified
                  ORDER BY bxi.avg_rating ASC, goal_counts.goals ASC, bxi.position_order DESC LIMIT 1
              );

            INSERT INTO best_xi_jornada (competition_id, season, jornada, player_id, position, position_order, avg_rating, matches_count)
            VALUES (p_competition_id, p_season, p_jornada, v_best_player.player_id, v_pos_classified, 1, v_best_player.avg_rating, v_best_player.matches_count);

            UPDATE best_xi_jornada SET position_order = sub.new_order
            FROM (
                SELECT bxi.player_id,
                       ROW_NUMBER() OVER (ORDER BY bxi.avg_rating DESC, COALESCE(goal_counts.goals, 0) DESC, bxi.player_id ASC) as new_order
                FROM best_xi_jornada bxi
                LEFT JOIN LATERAL (
                    SELECT COUNT(*)::INTEGER as goals FROM goal_events ge
                    INNER JOIN matches m ON ge.match_uuid = m.match_uuid
                    WHERE ge.player_id = bxi.player_id AND ge.event_type != 'own_goal'
                      AND m.competition_id = p_competition_id AND m.season = p_season AND m.round_id = p_jornada
                ) goal_counts ON true
                WHERE bxi.competition_id = p_competition_id AND bxi.season = p_season AND bxi.jornada = p_jornada
                  AND bxi.position = v_pos_classified
            ) sub
            WHERE best_xi_jornada.player_id = sub.player_id AND best_xi_jornada.competition_id = p_competition_id
              AND best_xi_jornada.season = p_season AND best_xi_jornada.jornada = p_jornada;
        END IF;
    END IF;
END;
$function$;

CREATE OR REPLACE FUNCTION calculate_best_xi_all_jornadas(p_competition_id integer)
 RETURNS void LANGUAGE plpgsql AS $function$
DECLARE
    v_season TEXT; v_jornada INTEGER;
BEGIN
    SELECT season INTO v_season FROM competitions WHERE id = p_competition_id;
    IF v_season IS NULL THEN RAISE EXCEPTION 'No se encontró la competición %', p_competition_id; END IF;
    FOR v_jornada IN
        SELECT DISTINCT round_id FROM matches
        WHERE competition_id = p_competition_id AND home_goals IS NOT NULL AND away_goals IS NOT NULL
          AND (resolved_administratively IS NULL OR resolved_administratively = false)
        ORDER BY round_id
    LOOP
        PERFORM calculate_best_xi_jornada(p_competition_id, v_season, v_jornada);
    END LOOP;
END;
$function$;

CREATE OR REPLACE FUNCTION calculate_mvp_all_jornadas(p_competition_id integer)
 RETURNS void LANGUAGE plpgsql AS $function$
DECLARE v_jornada INTEGER;
BEGIN
    FOR v_jornada IN
