-- ============================================================================
-- Schema slice para el spike de liga-offline (Fase 0).
-- Extraído por introspección del Supabase en vivo de `lliga` (proyecto
-- jdbjgrkvwawdibntngpk) el 2026-07-07, no de los ficheros de migración
-- (que están repartidos en 93 ALTER incrementales). Se omiten a propósito
-- las FKs hacia tablas fuera del slice (leagues, continents, auth.users,
-- profiles, users, national_teams, referees) porque pertenecen a sistemas
-- excluidos (managers/auth) o aún no portados en esta fase — las columnas
-- se conservan para compatibilidad futura, solo se quita la constraint.
-- ============================================================================

CREATE TYPE competition_type_enum AS ENUM ('league', 'cup', 'mixed', 'ranked');

-- ─────────────────────────────────────────────────────────────────────
CREATE TABLE seasons (
  id         bigint PRIMARY KEY,
  name       text NOT NULL UNIQUE,
  is_active  boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE clubs (
  id           bigint PRIMARY KEY,
  name         text NOT NULL,
  short_name   text,
  tla          text,
  crest_url    text,
  country      text,
  venue        text,
  created_at   timestamptz DEFAULT now(),
  address      text,
  website      text,
  founded      integer,
  club_colors  text,
  league_id    integer,   -- sin FK: `leagues` fuera del slice
  continent_id integer,   -- sin FK: `continents` fuera del slice
  pes_name     text,
  city         text
);

CREATE TABLE players (
  id                  bigint PRIMARY KEY,
  name                text NOT NULL,
  position            text,
  date_of_birth       date,
  nationality         text,
  created_at          timestamptz DEFAULT now(),
  efootball_id        integer,
  efootball_overall   smallint,
  efootball_potential smallint,
  efootball_cost_gp   integer
);

CREATE TABLE competitions (
  id                    integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name                  text NOT NULL,
  slug                  text NOT NULL UNIQUE,
  description           text,
  season                text NOT NULL,
  is_official           boolean DEFAULT false,
  is_public             boolean DEFAULT false,
  status                text DEFAULT 'draft' CHECK (status = ANY (ARRAY['draft','open','active','finished'])),
  max_teams             integer NOT NULL DEFAULT 2 CHECK (max_teams >= 2 AND max_teams <= 32),
  min_teams             integer DEFAULT 2,
  registration_deadline date,
  start_date            date,
  end_date              date,
  created_by            uuid,   -- sin FK: auth.users fuera del slice (managers/auth excluido)
  created_at            timestamptz DEFAULT now(),
  updated_at            timestamptz DEFAULT now(),
  competition_type      competition_type_enum DEFAULT 'league',
  config                jsonb DEFAULT '{}'::jsonb,
  creator_id            uuid,   -- sin FK: profiles fuera del slice
  type_config           jsonb NOT NULL DEFAULT '{}'::jsonb,
  theme_preset          text DEFAULT 'default' CHECK (theme_preset = ANY (ARRAY['default','christmas','halloween'])),
  theme_colors          jsonb,
  logo_url              text,
  parent_competition_id integer REFERENCES competitions(id) ON DELETE SET NULL
);

CREATE TABLE rounds (
  id             integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season         text NOT NULL,
  number         integer NOT NULL,
  name           text,
  competition_id integer REFERENCES competitions(id),
  UNIQUE (competition_id, season, number)
);

CREATE TABLE league_teams (
  id                integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  season            text NOT NULL,
  user_id           integer,   -- sin FK: `users` fuera del slice (managers/auth excluido)
  club_id           bigint REFERENCES clubs(id) ON DELETE SET NULL,
  nickname          text NOT NULL,
  display_name      text,
  created_at        timestamptz DEFAULT now(),
  penalty_points    integer NOT NULL DEFAULT 0,
  penalty_reason    text,
  updated_at        timestamptz DEFAULT now(),
  created_by        uuid,   -- sin FK: auth.users fuera del slice
  updated_by        uuid,   -- sin FK: auth.users fuera del slice
  competition_id    integer NOT NULL REFERENCES competitions(id),
  national_team_id  bigint,  -- sin FK: `national_teams` fuera del slice
  team_kind         text NOT NULL DEFAULT 'club' CHECK (team_kind = ANY (ARRAY['club','national'])),
  CONSTRAINT league_teams_kind_xor CHECK (
    (team_kind = 'club' AND club_id IS NOT NULL AND national_team_id IS NULL) OR
    (team_kind = 'national' AND national_team_id IS NOT NULL AND club_id IS NULL)
  )
);

CREATE TABLE matches (
  id                        text NOT NULL,
  season                    text NOT NULL,
  -- SIN FK real a rounds(id) a propósito: competition-schedule-generator.js
  -- (generateLeagueSchedule, camino round-robin estándar, sin modificar)
  -- escribe aquí el número de jornada directamente vía INSERT sin crear
  -- filas en `rounds` — con la FK puesta, ese INSERT falla siempre (rounds
  -- vacía). Coincide con lo descrito en la memoria jornada-round-id-vs-number
  -- ("coincidían por azar en comp 1"). Consecuencia aceptada: las páginas que
  -- hacen embed `round:rounds!matches_round_id_fkey(...)` (p.ej. algún
  -- widget de próximo/último partido) reciben 400 de PostgREST porque no
  -- hay relación de FK que resolver — ya degradan con try/catch, igual que
  -- el hueco conocido de team_fanbase_state.
  round_id                  integer,
  home_league_team_id       integer REFERENCES league_teams(id) ON DELETE SET NULL,
  away_league_team_id       integer REFERENCES league_teams(id) ON DELETE SET NULL,
  match_date                date,
  match_time                time,
  home_goals                integer CHECK (home_goals IS NULL OR home_goals >= 0),
  away_goals                integer CHECK (away_goals IS NULL OR away_goals >= 0),
  stream_url                text,
  created_at                timestamptz DEFAULT now(),
  updated_at                timestamptz DEFAULT now(),
  created_by                uuid,  -- sin FK: auth.users fuera del slice
  updated_by                uuid,  -- sin FK: auth.users fuera del slice
  is_played                 boolean,
  competition_id            integer REFERENCES competitions(id),
  round_type                varchar(20) DEFAULT 'league',
  group_name                varchar(50),
  cup_round                 integer,
  cup_leg                   varchar(20),
  is_cup_final              boolean DEFAULT false,
  match_uuid                integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  is_third_place_match      boolean NOT NULL DEFAULT false,
  has_penalties             boolean DEFAULT false,
  penalties_winner_id       integer REFERENCES league_teams(id),
  bracket_type              text CHECK (bracket_type = ANY (ARRAY['winner','loser'])),
  resolved_administratively boolean DEFAULT false,
  highlights_url            text,
  referee_id                bigint,  -- sin FK: `referees` fuera del slice (se porta en Fase 1)
  UNIQUE (competition_id, id)
);

